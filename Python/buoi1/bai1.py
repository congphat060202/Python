# Câu 1. Nhập vào 2 xâu ký tự a,b.
# a. Hiển thị 2 xâu a,b trên cùng 1 dòng 
# a. Hiển thị 2 xâu a,b trên 2 dòng 
# a. Hiển thị xâu a lặp lại 5 lần trên 1 dòng 
# a. Hiển thị xâu b lặp lại 5 lần trên 5 dòng 
# Nhập 2 xâu ký tự a và b từ người dùng
a = input("Nhập xâu a: ")
b = input("Nhập xâu b: ")

# Hiển thị 2 xâu a và b trên cùng 1 dòng
print("a:", a, "b:", b)

# Hiển thị 2 xâu a và b trên 2 dòng
print("a:", a)
print("b:", b)

# Hiển thị xâu a lặp lại 5 lần trên 1 dòng
print("a lặp lại 5 lần:", a * 5)

# Hiển thị xâu b lặp lại 5 lần trên 5 dòng
print("b lặp lại 5 lần:", (b + "\n")*5)

