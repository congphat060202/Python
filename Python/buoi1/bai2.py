# Nhập hai số a và b từ bàn phím
a = int(input("Nhập số a: "))
b = int(input("Nhập số b: "))

# In loại dữ liệu của a và b
print("Loại dữ liệu của a:", type(a))
print("Loại dữ liệu của b:", type(b))

# Tính tổng
tong = a + b
print("Tổng của a và b:", tong)

# Tính tổng
hieu = a - b
print("Hiệu của a và b:", hieu)

# Tính tổng
tich = a * b
print("Tích của a và b:", tich)

# Tính tổng
thuong = a / b
